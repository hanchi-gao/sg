			<h3>主創團隊</h3>
            <div >藤子．F．不二雄不只是大家常知的兒童漫畫家。藤子老師的世界有兩大基礎，「兒童文學與日常 SF」。自 1950 年代後期一直到 1960 年代前期為主的作品，是藤子·F·不二雄在兒童雜誌上站穩基礎並發展自己漫畫特色的重要時代。在藤子·F·不二雄創作的作品中，固然有像《哆啦A夢》這樣面對兒童創作的漫畫，但另一方面他也在《ビッグコミック》這類面對青年或成人的漫畫雜誌推出各式各樣的「SF 異色短篇集」，「SF」是指「一小點的不可思議」的意思，而短篇的內容以漫畫的形式嘲諷現實，因此藤子老師可看的作品絕對不只是《哆啦A夢》，更是一名具有廣度與深度的漫畫家。
